A = int(input("Ввести число А :"))
B = int(input("Ввести число B :"))

if A ** 2 + B ** 2 > (A + B) ** 2:
    print("Сумма квадратов больше", A ** 2 + B ** 2, ">", (A + B) ** 2)
elif A ** 2 + B ** 2 < (A + B) ** 2:
    print("Квадрат суммы больше", A ** 2 + B ** 2, "<", (A + B) ** 2)
else:
    print("Сумма квадратов и квадрат суммы равны", A ** 2 + B ** 2, "=", (A + B) ** 2)